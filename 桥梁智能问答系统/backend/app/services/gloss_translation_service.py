class GlossTranslationService:
    @staticmethod
    def gloss2text(gloss_text: str):
        # 这里需要实现实际的 Gloss 转文本逻辑
        return f"Translated from Gloss: {gloss_text}"

    @staticmethod
    def text2gloss(input_text: str):
        # 这里需要实现实际的文本转 Gloss 逻辑
        return f"Converted to Gloss: {input_text}"